  <script>
      document.querySelector('.follow-btn').addEventListener('click', function(event) {
          event.preventDefault();
  
          Swal.fire({
              title: 'طلب سحب الرصيد', 
              html: `
                <form id="withdrawForm" action="{{ route('withdraw.post') }}" method="POST" style="display: flex; flex-direction: column; gap: 10px;">
                  @csrf
                  <input type="number" name="amount" min="0" step="1" required placeholder="ادخل المبلغ" style="padding: 10px; border: 1px solid #ccc; border-radius: 5px;">
                  <input type="text" name="method" required placeholder="ادخل رقم الحساب البنكي" style="padding: 10px; border: 1px solid #ccc; border-radius: 5px;">
                </form>
              `,
              showCancelButton: true,
              confirmButtonText: 'سحب',
              cancelButtonText: 'الغاء',
              focusConfirm: false,
              preConfirm: () => {
                  const form = document.getElementById('withdrawForm');
                  if (!form.amount.value || !form.method.value) {
                      Swal.showValidationMessage('يرجى إدخال جميع الحقول المطلوبة');
                      return false;
                  }
                  return new Promise(function (resolve) {
                      form.submit();
                      resolve();
                  });
              }
          }).then((result) => {
              if (result.isConfirmed) {
                  Swal.fire('تم ', 'تم تقديم طلب السحب بنجاح.', 'success');
              }
          });
      });
  </script>

